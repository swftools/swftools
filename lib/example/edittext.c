#define addGlyph fn1cb9

void fn1cb9(SWFFONT * f,int i,U16 ascii,U16 advance,U8 * data,U32 bitlen,int xmin,int ymin,int xmax, int ymax)
{ SHAPE * s;
  U32 l = (bitlen+7)/8;

  if (FAILED(swf_ShapeNew(&s))) return;
  s->data = malloc(l);
  if (!s->data) { swf_ShapeFree(s); return; }

  f->glyph2ascii[i]     = ascii;
  f->ascii2glyph[ascii] = i;
  f->glyph[i].advance   = advance;
  f->glyph[i].shape     = s;
  s->bitlen             = bitlen;
  s->bits.fill          = 1;
  if(f->layout && f->layout->bounds)
  {  f->layout->bounds[i].xmin = xmin;
     f->layout->bounds[i].ymin = ymin;
     f->layout->bounds[i].xmax = xmax;
     f->layout->bounds[i].ymax = ymax;
  }
  memcpy(s->data,data,l);
}

